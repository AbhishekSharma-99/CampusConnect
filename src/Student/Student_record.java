    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String t1 = txtSearch.getText().trim();
        if (t1.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Please enter a student ID before searching.");
            return;
        }   
        
        Connection conn = DatabaseConnection.getConnection();
        PreparedStatement pstmt = null;
        ResultSet rs = null;        
        try {
            // Create a prepared statement object
            pstmt = conn.prepareStatement("SELECT * FROM Student WHERE student_id = ?");

            // Set the search value as a parameter
            pstmt.setString(1, t1);

            // Execute the query
            rs = pstmt.executeQuery();

            // Get the table model from the JTable
            DefaultTableModel model = (DefaultTableModel) Student_Table.getModel();

            // Clear the table model
            model.setRowCount(0);

            // Iterate through the result set and add the data to the table model
            while (rs.next()) {
                javax.swing.JOptionPane.showMessageDialog(null, "Printing to be start... ... ...");
                model.addRow(new Object[] {
                    rs.getString("student_id"),
                    rs.getString("FirstName"),
                    rs.getString("LastName"),
                    rs.getString("DOB"),
                    rs.getString("Gender"),
                    rs.getString("Father_Name"),
                    rs.getString("F_Occupation"),
                    rs.getString("Mother_Name"),
                    rs.getString("M_Occupation"),
                    rs.getString("Address"),
                    rs.getString("Contact_no"),
                    rs.getString("Batch"),
                    rs.getString("Branch"),
                    rs.getString("Year")
                    // Add more columns as needed  
                });
                javax.swing.JOptionPane.showMessageDialog(null, "Printing to be Stop... ... ...!");
            }

        } catch (SQLException e) {
            System.out.println("Error executing query: " + e.getMessage());
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                System.out.println("Error closing database resources: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

