    private void loadDataIntoTable() {

        try {
            // Connect to the database
            Connection conn = DatabaseConnection.getConnection();

            // Create a prepared statement
            String query = "SELECT * FROM Student";
            PreparedStatement pstmt = conn.prepareStatement(query);

            ResultSet resultSet;
            // Execute a query to retrieve the data
            resultSet = pstmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) Student_Table.getModel();
            // Clear the table model
            model.setRowCount(0);

            // Load the data into the table model
            while (resultSet.next()) {
                Object[] row = new Object[14];
                row[0] = resultSet.getString("student_id");
                row[1] = resultSet.getString("FirstName");
                row[2] = resultSet.getString("LastName");
                row[3] = resultSet.getString("DOB");
                row[4] = resultSet.getString("Gender");
                row[5] = resultSet.getString("Father_Name");
                row[6] = resultSet.getString("F_Occupation");
                row[7] = resultSet.getString("Mother_Name");
                row[8] = resultSet.getString("M_Occupation");
                row[9] = resultSet.getString("Address");
                row[10] = resultSet.getString("Contact_No");
                row[11] = resultSet.getString("Batch");
                row[12] = resultSet.getString("Branch");
                row[13] = resultSet.getString("Year");
                model.addRow(row);
            }

            // Close the result set, statement, and connection
            resultSet.close();
            pstmt.close();
            conn.close();
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading data into table: " + e.getMessage());
        }
    }
    
